<style scoped lang="scss">
    @import "../styles/variables";
    @import "../styles/buttons";

    .bc-text-field {
        display: inline-flex;
        flex-direction: column;
    }
    .options-container {
        display: inline-flex;
        align-items: center;
    }
</style>

<template>
    <div>
        <div class="bc-text-field margin-left">
            <div class="options-container">
                <select class="inp" v-model="field">
                    <option value="" disabled>Selecione</option>
                    <option v-for="(date, idx) in options" :key="idx" :value="date">
                            {{date.value}}
                    </option>
                </select>
            </div>
        </div>
    </div>
</template>

<script>

    import bcService from '../services/bc-services.js';

    export default {
        name: 'bc-text-combo',
        props: {
            val: Array
        },
        data() {
            return {
                field: this.val[0] || '',
                options: []
            };
        },
        created() {
            // ajustar request (est√° retornando dados mocados)
            this.options = bcService.getTipoSelecaoOptions();
        },
        methods: {
            handleValue() {
                if (this.field === '') {
                    return this.$emit('change', null);
                }
                return this.$emit('change', [this.field.id]);
            }
        },
        watch: {
            field() {
                this.handleValue();
            }
        }
    };
</script>
